<p>App: Squalid Salad</p>
<p>Dataset: Unintentional injuries in Canada 2010</p>
<p>Canadian Hospitals Injury Reporting and Prevention Program (CHIRPP) is a computerized information system that collects and analyzes data on injuries to people (mainly children) who are seen at the emergency rooms of the 10 pediatric hospitals and of 4 general hospitals in Canada. CHIRPP is a unique, richly detailed database of "pre-event" injury information obtained by asking:What was the injured person doing when the injury happened?, What went wrong? and Where did the injury occur?</p>
<p><a href="http://data.gc.ca/data/en/dataset/3a78ceb8-fd71-40dd-9f42-1c1af158bf0f">http://data.gc.ca/data/en/dataset/3a78ceb8-fd71-40dd-9f42-1c1af158bf0f</a></p>
<p>I also used child safety information from <a href="http://www.childsafetylink.ca/">http://www.childsafetylink.ca/</a></p>
<p>Limitations: The age range was not fined grained enough to give different percentage of injuries for each age group listed. The raw data would have been helpful as well to sort the advice. I can not say which type of injury is most likely to occur in which room with the data I have now.</p>
